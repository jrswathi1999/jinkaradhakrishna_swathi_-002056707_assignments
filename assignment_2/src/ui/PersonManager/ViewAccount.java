    private void refreshTextFields() {
        routingtxtv.setText(account.getRountingNumber());
        accounttxtv.setText(account.getAccountNumber());
        banknametxtv.setText(account.getBankName());
    }

    private void setViewMode() {
        routingtxtv.setEnabled(false);
        accounttxtv.setEnabled(false);
        banknametxtv.setEnabled(false);
        
        savebtnv.setEnabled(false);
        updatebtnv.setEnabled(true);
        //update button is set to work only in view mode
    }
    
    private void setEditMode() {
        routingtxtv.setEnabled(true);
        accounttxtv.setEnabled(true);
        banknametxtv.setEnabled(true);
        
        savebtnv.setEnabled(true);
        updatebtnv.setEnabled(false);
        //editing routing no, account no, bank name save button is allowed only in edit mode
    }

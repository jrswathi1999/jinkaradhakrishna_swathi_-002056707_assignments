    private void savebtnvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnvActionPerformed
        // TODO add your handling code here:
        // getting text from text fields
        String routingNumber= routingtxtv.getText();
        String accountNumber = accounttxtv.getText();
        String bankName = banknametxtv.getText();
        
        if(routingNumber.isBlank() || accountNumber.isBlank() || bankName.isBlank())
        {//verifying if text feilds are blank
            JOptionPane.showMessageDialog(null,"All fields are mandatory");
            return;
        }
        
        //updating the directory
        account.setRountingNumber(routingNumber);
        account.setAccountNumber(accountNumber);
        account.setBankName(bankName);
        
        JOptionPane.showMessageDialog(null,"Account update successful","Sucess",JOptionPane.WARNING_MESSAGE);
               
        setViewMode();     
        
        
    }//GEN-LAST:event_savebtnvActionPerformed


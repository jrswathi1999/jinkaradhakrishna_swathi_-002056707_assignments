    private void savebtnvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnvActionPerformed
        // TODO add your handling code here:
        // getting text from text fields
        
          //getting values for     
        long SSN;
        int age;
        double weight;
        
        String firstName= txtFirstName.getText();
        String lastName= txtLastName.getText();
        
        try{
            SSN= Long.parseLong(txtSSN.getText());
            age = Integer.parseInt(txtAge.getText());
            weight = Double.parseDouble(txtWeight.getText());
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please check the SSN , age and weight input. They should me numbers", "Warning", JOptionPane.WARNING_MESSAGE);
            return;//validation for balance input text field 
        }
        
        
        // geting test feilds of home address
         String hStreet = HtxtStreetAdd.getText();
         String hUnit = HtxtUnitNo.getText();
         String hCity = HtxtCity.getText();
         String hState = HtxtState.getText();
         String hZip = HtxtZip.getText();       
         String hPhone = HtxtPhone.getText();
        
        //getting values of work address
        String wStreet = WtxtStreetAdd2.getText();
        String wUnit = WtxtUnitNo2.getText();
        String wCity = WtxtCity2.getText();
        String wState = WtxtState2.getText();
        String wZip = WtxtZip2.getText();
        String wPhone = WtxtPhone2.getText();
        
        
      
         if(firstName.isBlank() || lastName.isBlank())
        {
            JOptionPane.showMessageDialog(this,"All fields are mandatory","Error",JOptionPane.ERROR_MESSAGE);
            //validification for create person , checking if the text fields are empty
            //stops the process if it's empty
            return;
        }
        
        
        // validation for home address and work address
         
        if(hStreet.isBlank()||hUnit.isBlank()||hCity.isBlank()||hState.isBlank()||hZip.isBlank()||hPhone.isBlank()){
            JOptionPane.showMessageDialog(this,"All work address feilds are mandatory","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(wStreet.isBlank()|| wUnit.isBlank()|| wCity.isBlank()||wState.isBlank()||wZip.isBlank()|| wPhone.isBlank()){
            JOptionPane.showMessageDialog(this,"All work address feilds are mandatory","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        //updating the directory
                    
         
        
        
        //setting person from save button
        person.setFirstName(firstName);
        person.setLastName(lastName);
        person.setSSN(SSN);
        person.setAge(age);
       
        
        person.getHomeAddress().setStreetAddress(hStreet);
        person.getHomeAddress().setUnitNumber(hUnit);
        person.getHomeAddress().setCity(hCity);
        person.getHomeAddress().setState(hState);
        person.getHomeAddress().setZipCode(hZip);
        person.getHomeAddress().setPhoneNumber(hPhone);


        person.getWorkAddress().setStreetAddress(wStreet);
        person.getWorkAddress().setUnitNumber(wUnit);
        person.getWorkAddress().setCity(wCity);
        person.getWorkAddress().setState(wState);
        person.getWorkAddress().setZipCode(wZip);
        person.getWorkAddress().setPhoneNumber(wPhone);
        
        
        //address objects for home and work
        
//        Address homeAddup = new Address(hStreet, hUnit, hCity,hState,hZip,hPhone);
//        Address workAddup = new Address(wStreet, wUnit, wCity,wState,wZip,wPhone);
//        
        //setting home address and work address for the person class
        
       //dialog box for profile success messsge
        JOptionPane.showMessageDialog(null,"Account update successful","Sucess",JOptionPane.WARNING_MESSAGE);
        setViewMode();  
        
    }//GEN-LAST:event_savebtnvActionPerformed

